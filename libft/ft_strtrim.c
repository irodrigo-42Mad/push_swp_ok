#include "libft.h"

char	*ft_strtrim(const char *s, const char *set)
{
	char	*ret;
	size_t	len;

	if (!s)
		return (NULL);
	while (*s && ft_strchr(set, *s))
		s++;
	len = ft_strlen(s);
	while (len && ft_strchr(set, s[len]))
		len--;
	ret = ft_substr(s, 0, len + 1);
	return (ret);
}
